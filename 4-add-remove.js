const fruits = ['apple', 'banana', 'cherry']
console.log(fruits)

fruits.push('melon')    // นำข้อมูลเพิ่มไว้ด้านท้าย
fruits.unshift('grape') // นำข้อมูลเพิ่มไว้ด้านหน้า
console.log(fruits)

fruits.pop()            // นำข้อมูลด้านหลังออก
console.log(fruits)

fruits.shift()          // นำข้อมูลด้านหน้าออก
console.log(fruits)
